package org.example;

public class Address {
    public Address(String id, String street, String city, String state, String zip) {
        this.id = id;
        this.street = street;
        this.city = city;
        this.state = state;
        this.zip = zip;
    }

    public String id;
    public String street;
    public String city;
    public String state;
    public String zip;
}
